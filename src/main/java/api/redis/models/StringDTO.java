package api.redis.models;

public class StringDTO {
    String key;
    String data;

    public StringDTO() {
    }

    public StringDTO(String key, String data) {
        this.key = key;
        this.data = data;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
